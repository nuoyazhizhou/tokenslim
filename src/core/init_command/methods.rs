/// 打印初始化结果摘要
pub fn print_init_summary(result: &InitResult) {
    println!("\n{}", t("core_init_summary_title"));
    println!("{}", t("core_init_summary_separator"));
    println!();

    if result.config_created {
        println!("{}", t1("core_init_config_created", &result.config_path));
    } else {
        println!(
            "{}",
            t1("core_init_config_existing_or_dry", &result.config_path)
        );
    }

    println!();
    println!("{}", t("core_init_project_detection"));
    println!("   {}", t1("core_init_project_type", &result.project_type));
    if let Some(fw) = &result.framework {
        println!("   {}", t1("core_init_project_framework", fw));
    }
    if let Some(pm) = &result.package_manager {
        println!("   {}", t1("core_init_project_package_manager", pm));
    }

    println!();
    if result.hooks_installed {
        println!("{}", t("core_init_hooks_installed"));
    } else {
        println!("{}", t("core_init_hooks_skipped"));
    }

    println!();
    println!("{}", t("core_init_next_steps"));
    println!("   {}", t("core_init_next_step1"));
    println!("   {}", t("core_init_next_step2"));
    println!("   {}", t("core_init_next_step3"));
    println!("   {}", t("core_init_next_step4"));
    println!("   {}", t("core_init_next_step5"));
    println!();

    let config_dir = std::path::Path::new("config").join("plugins");
    let capabilities =
        crate::core::plugin_config_loader::get_all_plugin_capabilities(if config_dir.exists() {
            Some(&config_dir)
        } else {
            None
        });
    if !capabilities.is_empty() {
        println!("🚀 Active Plugins:");
        let names: Vec<String> = capabilities.into_iter().map(|c| c.name).collect();
        println!("   {}", names.join(", "));
        println!();
    }
}
