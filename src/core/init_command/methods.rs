/// 执行初始化命令
pub fn run_init(options: InitOptions) -> Result<InitResult, String> {
    let cwd = std::env::current_dir().map_err(|e| format!("{E_INIT_CURRENT_DIR}:{e}"))?;

    // 检测项目信息
    let project_info = detect_project_type(&cwd);

    // 生成配置文件
    let config_path = cwd.join(".tokenslim.toml");
    let config_created = if config_path.exists() && !options.force {
        false
    } else {
        let config_content = generate_config(&project_info);
        if options.dry_run {
            println!("{}", config_content);
            false
        } else {
            fs::write(&config_path, config_content)
                .map_err(|e| format!("{E_INIT_WRITE_CONFIG}:{e}"))?;
            true
        }
    };

    // 安装 shell hooks (可选)
    let hooks_installed = if options.install_hooks {
        let shell = options.hook_shell.clone().unwrap_or_else(detect_shell);
        match install_hooks_simple(&shell, options.dry_run) {
            Ok(_) => true,
            Err(e) => {
                if e == E_INIT_HOOKS_ALREADY_INSTALLED {
                    // Silent success or print info
                    true
                } else {
                    eprintln!("{}", t1("core_init_hooks_install_failed", e));
                    false
                }
            }
        }
    } else {
        false
    };

    // 构建结果消息
    let mut message = t("core_init_success").to_string();
    if !config_created {
        if config_path.exists() {
            message.push_str(t("core_init_config_exists_force"));
        } else {
            message.push_str(t("core_init_dry_run_no_files"));
        }
    }

    Ok(InitResult {
        config_created,
        config_path: config_path.to_string_lossy().to_string(),
        project_type: project_info.primary.clone(),
        framework: project_info.framework.clone(),
        package_manager: project_info.package_manager.clone(),
        hooks_installed,
        message,
    })
}

