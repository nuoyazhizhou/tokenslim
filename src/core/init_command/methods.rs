/// 简化的项目检测 (复用 doctor_workspace 的逻辑)
fn detect_project_type(cwd: &Path) -> ProjectInfo {
    let has = |name: &str| cwd.join(name).exists();
    let mut langs = Vec::<String>::new();

    if has("Cargo.toml") {
        langs.push("rust".to_string());
    }
    if has("pom.xml") || has("build.gradle") || has("build.gradle.kts") {
        langs.push("java".to_string());
    }
    if has("package.json") {
        langs.push("node".to_string());
    }
    if has("pyproject.toml") || has("requirements.txt") || has("setup.py") {
        langs.push("python".to_string());
    }
    if has("CMakeLists.txt") || has("Makefile") {
        langs.push("cpp".to_string());
    }
    if has("go.mod") {
        langs.push("go".to_string());
    }
    if has("Gemfile") {
        langs.push("ruby".to_string());
    }
    if has("composer.json") {
        langs.push("php".to_string());
    }
    if has("pubspec.yaml") {
        langs.push("dart".to_string());
    }
    if has("Package.swift") {
        langs.push("swift".to_string());
    }

    let primary = langs
        .first()
        .cloned()
        .unwrap_or_else(|| "unknown".to_string());
    let mut framework = None;
    let mut package_manager = None;

    // 检测 Node.js 框架和包管理器
    if primary == "node" {
        if has("pnpm-lock.yaml") {
            package_manager = Some("pnpm".to_string());
        } else if has("yarn.lock") {
            package_manager = Some("yarn".to_string());
        } else if has("bun.lockb") || has("bun.lock") {
            package_manager = Some("bun".to_string());
        } else {
            package_manager = Some("npm".to_string());
        }

        if has("next.config.js") || has("next.config.mjs") || has("next.config.ts") {
            framework = Some("nextjs".to_string());
        } else if has("vue.config.js") {
            framework = Some("vue".to_string());
        } else if has("nuxt.config.js") || has("nuxt.config.ts") {
            framework = Some("nuxt".to_string());
        } else if has("svelte.config.js") {
            framework = Some("svelte".to_string());
        } else if has("angular.json") {
            framework = Some("angular".to_string());
        }
    } else if primary == "rust" {
        if has("src-tauri/tauri.conf.json") {
            framework = Some("tauri".to_string());
        }
    } else if primary == "python" {
        let reqs = fs::read_to_string(cwd.join("requirements.txt")).unwrap_or_default();
        if has("manage.py") {
            framework = Some("django".to_string());
        } else if reqs.contains("fastapi") {
            framework = Some("fastapi".to_string());
        } else if reqs.contains("flask") {
            framework = Some("flask".to_string());
        }
    } else if primary == "dart" {
        if let Ok(content) = fs::read_to_string(cwd.join("pubspec.yaml")) {
            if content.contains("sdk: flutter") || content.contains("flutter:") {
                framework = Some("flutter".to_string());
            }
        }
    }

    ProjectInfo {
        primary,
        framework,
        package_manager,
    }
}

/// 简化的 shell 检测
fn detect_shell() -> String {
    // Windows 检测
    if cfg!(target_os = "windows") {
        if std::env::var("POWERSHELL_DISTRIBUTION_CHANNEL").is_ok() {
            return "powershell".to_string();
        }
        if std::env::var("SHELL")
            .map(|s| s.contains("bash"))
            .unwrap_or(false)
        {
            return "bash".to_string();
        }
        return "cmd".to_string();
    }

    // Unix 检测
    std::env::var("SHELL")
        .ok()
        .and_then(|s| {
            let shell = s.rsplit('/').next().unwrap_or("");
            match shell {
                "bash" | "zsh" | "fish" => Some(shell.to_string()),
                _ => None,
            }
        })
        .unwrap_or_else(|| "bash".to_string())
}

/// 简化的 hook 安装 (复用现有逻辑)
fn install_hooks_simple(shell: &str, dry_run: bool) -> Result<(), String> {
    // cmd 特殊处理：写 doskey 文件 + 设置 AutoRun 注册表
    if shell == "cmd" {
        return install_hooks_cmd(dry_run);
    }

    let shell_config = match shell {
        "bash" => ".bashrc",
        "zsh" => ".zshrc",
        "fish" => ".config/fish/config.fish",
        "powershell" => {
            if cfg!(target_os = "windows") {
                "Documents/PowerShell/Microsoft.PowerShell_profile.ps1"
            } else {
                ".config/powershell/Microsoft.PowerShell_profile.ps1"
            }
        }
        _ => ".bashrc",
    };

    let home = std::env::var("HOME")
        .or_else(|_| std::env::var("USERPROFILE"))
        .unwrap_or_else(|_| ".".to_string());

    let config_path = Path::new(&home).join(shell_config);

    if dry_run {
        println!(
            "{}",
            t1("core_init_dry_run_install_path", config_path.display())
        );
        return Ok(());
    }

    // 检查配置文件是否存在
    if !config_path.exists() {
        // 创建空文件
        if let Some(parent) = config_path.parent() {
            fs::create_dir_all(parent).map_err(|e| format!("{E_INIT_CREATE_DIR}:{e}"))?;
        }
        fs::write(&config_path, "").map_err(|e| format!("{E_INIT_CREATE_CONFIG_FILE}:{e}"))?;
    }

    // 检查是否已经安装
    let content =
        fs::read_to_string(&config_path).map_err(|e| format!("{E_INIT_READ_CONFIG_FILE}:{e}"))?;

    if content.contains("# TokenSlim") {
        return Err(E_INIT_HOOKS_ALREADY_INSTALLED.to_string());
    }

    // 生成 hook 内容
    let hook_content = generate_hook_content(shell);

    // 追加到配置文件
    fs::write(&config_path, format!("{}\n{}", content, hook_content))
        .map_err(|e| format!("{E_INIT_APPEND_CONFIG_FILE}:{e}"))?;

    Ok(())
}

/// cmd (Windows) 钩子安装：写 doskey 宏文件 + 设置 AutoRun 注册表
fn install_hooks_cmd(dry_run: bool) -> Result<(), String> {
    let home = std::env::var("USERPROFILE").unwrap_or_else(|_| ".".to_string());
    let hook_path = Path::new(&home).join("tokenslim_hook.cmd");

    // 检测当前 tokenslim 可执行文件路径，确保 doskey 宏能正确调用
    let tokenslim_exe = std::env::current_exe()
        .map(|p| p.to_string_lossy().to_string())
        .unwrap_or_else(|_| "tokenslim".to_string());
    let tokenslim_cmd = if tokenslim_exe.ends_with(".exe") {
        tokenslim_exe
    } else {
        format!("{}.exe", tokenslim_exe)
    };

    if dry_run {
        println!(
            "{}",
            t1("core_init_dry_run_install_path", hook_path.display())
        );
        return Ok(());
    }

    // 检查是否已安装（通过 AutoRun 注册表）
    let reg_output = std::process::Command::new("reg")
        .args([
            "query",
            r"HKCU\Software\Microsoft\Command Processor",
            "/v",
            "AutoRun",
        ])
        .output();
    if let Ok(output) = reg_output {
        let stdout = String::from_utf8_lossy(&output.stdout);
        if stdout.contains("tokenslim_hook") {
            return Err(E_INIT_HOOKS_ALREADY_INSTALLED.to_string());
        }
    }

    // 写 doskey 宏文件（使用实际 tokenslim 路径）
    let hook_content = generate_hook_content_cmd(&tokenslim_cmd);
    fs::write(&hook_path, hook_content).map_err(|e| format!("{E_INIT_WRITE_HOOK_FILE}:{e}"))?;

    // 设置 AutoRun 注册表（追加到已有 AutoRun 之后）
    let existing = String::from_utf8_lossy(
        &std::process::Command::new("reg")
            .args([
                "query",
                r"HKCU\Software\Microsoft\Command Processor",
                "/v",
                "AutoRun",
            ])
            .output()
            .map(|o| o.stdout)
            .unwrap_or_default(),
    )
    .lines()
    .filter(|l| !l.trim().is_empty() && !l.starts_with("HKEY_") && !l.starts_with(' '))
    .last()
    .map(|l| l.trim().to_string())
    .unwrap_or_default();

    let auto_run_value = if existing.is_empty() {
        hook_path.to_string_lossy().to_string()
    } else {
        format!("{} && {}", existing, hook_path.display())
    };

    let status = std::process::Command::new("reg")
        .args([
            "add",
            r"HKCU\Software\Microsoft\Command Processor",
            "/v",
            "AutoRun",
            "/t",
            "REG_SZ",
            "/d",
            &auto_run_value,
            "/f",
        ])
        .status()
        .map_err(|e| format!("{E_INIT_SET_AUTORUN}:{e}"))?;

    if !status.success() {
        return Err(E_INIT_SET_AUTORUN_FAILED.to_string());
    }

    Ok(())
}

/// 生成 cmd hook 内容（使用实际 tokenslim 路径，避免 PATH 问题）
fn generate_hook_content_cmd(tokenslim_path: &str) -> String {
    format!(
        r#"@echo off
REM TokenSlim - AI Token Optimizer Hook
REM Auto-loaded via registry AutoRun.
REM doskey macros only effective in current cmd session (not subprocesses).
{}
{}
REM Aliases
doskey ts={tokenslim_path} $*
doskey ts-run={tokenslim_path} run $*
doskey ts-compress={tokenslim_path} compress $*
doskey ts-doctor={tokenslim_path} workspace $*
"#,
        VCS_COMMANDS
            .iter()
            .map(|c| format!("doskey {c}={tokenslim_path} run {c} $*"))
            .collect::<Vec<_>>()
            .join("\n"),
        BUILD_COMMANDS
            .iter()
            .map(|c| format!("doskey {c}={tokenslim_path} run {c} $*"))
            .collect::<Vec<_>>()
            .join("\n"),
    )
}

